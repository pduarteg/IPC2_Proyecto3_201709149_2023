function mostrarDivSolService() {
    var div = document.getElementById("solService");
    if (div.style.display === "none") {
        div.style.display = "block";
    } else {
        div.style.display = "none";
    }
}

function showStdInfo() {
    var div = document.getElementById("sdtInfo");
    if (div.style.display === "none") {
        div.style.display = "block";
    } else {
        div.style.display = "none";
    }
}